#Напишіть програму, що запитує довільне число у користувача і виводить його тангенс.
#Розрахувати тангенс за допомогою тільки math.cos (не можна використовувати вбудовані функції для розрахунку сінусу чи тангенсу -- потрібно втілити відповідні формули).

import math
a = float(input("Введіть число для тангенса: "))
tan = (1-(math.cos(a)**2) ** .5) / math.cos(a)
print(tan)